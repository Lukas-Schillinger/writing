	<h2 id="stuck-bolts">Stuck Bolts</h2>
	<p>
		Occasionally you will encounter a bolt that will not break loose. Before you
		try pulling even harder consider the damage you may be doing to the head of
		the bolt. Applying too much force to the head of a bolt may begin to round
		the edges making it even more difficult to remove. Taking your car to an
		auto shop and having a professional remove the stuck bolt is usually the
		best option for amateurs.
