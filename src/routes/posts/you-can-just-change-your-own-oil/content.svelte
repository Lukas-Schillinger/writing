	<h1 id="instructions">Instructions</h1>
	<p>
		The best way to teach yourself how to change your engine oil is to find a
		tutorial online. Googling '<strong
			><strong><strong><em>*</em></strong></strong></strong
		>your car year, make, model<strong
			><strong><strong><em>*</em></strong></strong></strong
		> oil change' will usually yield hundreds of results. Cars are manufactured in
		'generations' where the same construction is used for around 5 years. Finding
		a tutorial for a car of the same make and model as yours with a different year
		but within the same generation will usually be sufficient.
	</p>
	<p>
		The instructions given below are not exhaustive and should be considered a
		supplement to a video tutorial that is specific to your vehicle.
	</p>
	<h3 id="1-lift-car-">1. <strong>Lift Car</strong></h3>
	<p>Elevate your car using your preferred method.</p>
	<h3 id="2-drain-oil-">2. <strong>Drain Oil</strong></h3>
	<p>
		Position your oil collection vessel below the vehicle's oil pan drain plug
		bolt. If the bolt is at an angle ensure that the oil collection vessel is
		positioned to account for the trajectory of the draining oil. A good way to
		control the flow of oil is to keep the drain plug bolt pressed against the
		oil pan while you're unscrewing it. This will prevent the oil from dripping
		over your arms and tools.
	</p>
	<p>
		The oil will initially come out very quickly and eventually slow to a drip.
		You should wait until all the oil has been emptied before putting the drain
		plug bolt back on. The oil filter can be replaced while waiting for the oil
		pan to drain.
	</p>
	<h3 id="3-replace-filter">3. Replace Filter</h3>
	<p>
		The specifics of replacing your oil filter will depend on the vehicle.
		Before removing your oil filter note the small pieces, if any, that are
		packaged with the filter. Most oil filter kits will include a rubber O-ring
		used to maintain a seal between the oil filter housing and your vehicle.
		Find these parts on your old filter once removed and replace them with the
		new ones.
	</p>
	<p>
		Keep in mind that the filter will be saturated in oil and care should be
		taken that it won't spill when being removed. Oil will drain from where the
		filter housing was removed but this should subside before the oil pan has
		been drained.
	</p>
	<h3 id="4-re-plug-oil-pan">4. Re-Plug Oil Pan</h3>
	<p>
		Return the oil drain plug bolt back into the oil pan. Tighten the bolt using
		your socket wrench until you meet resistance plus one little shove.
	</p>
	<h3 id="5-fill-car-with-new-oil">5. Fill Car with New Oil</h3>
	<p>
		Your car's oil capacity can be found in the owner's manual or by googling '<strong
			><strong><strong><strong>**</strong></strong></strong></strong
		>your car year, make, model<strong
			><strong><strong><strong>**</strong></strong></strong></strong
		> oil capacity'. Before you begin filling your car with oil consider whether
		or not the oil drain plug is in and the oil filter is secured.
	</p>
	<h3 id="6-check-oil-level">6. Check Oil Level</h3>
	<p>
		Your car will have a rod used to test the oil level of your vehicle called a
		dipstick. You can usually find the dipstick by looking for a yellow ring
		somewhere in the engine bay.
	</p>
	<p>
		The bottom of the dipstick sits partway inside the oil tank and will have
		markings indicating the recommended oil level. Before checking your engine's
		oil level see if your owner's manual recommends checking after running the
		engine or if it can be checked cold. Also make sure your car is level and no
		longer elevated.
	</p>
