	<h2 id="socket-wrench">Socket Wrench</h2>
	<p>
		Engine oil sits in a large tank accessible from the underbelly of your car
		called an oil pan. Somewhere on the oil pan is a drain that is kept plugged
		with a small drain plug bolt. Removing the drain plug bolt requires a socket
		wrench. The socket size required to fit the drain plug bolt is not
		universal. Any socket wrench set will include a socket that fits your drain
		plug.
